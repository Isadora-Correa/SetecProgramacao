        // Variável global para armazenar o dia atualmente selecionado
        let currentDayFilter = 'todos';

        function filterDay(day) {
            // 1. Atualiza o filtro de dia
            currentDayFilter = day;

            // 2. Atualiza os botões (visual)
            const buttons = document.querySelectorAll('.filter-btn');
            buttons.forEach(btn => btn.classList.remove('active'));
            // Encontra o botão que corresponde ao dia e o ativa
            const activeBtn = document.querySelector(`.filter-btn[onclick="filterDay('${day}')"]`);
            if (activeBtn) {
                activeBtn.classList.add('active');
            }

            // 3. Chama a função de filtragem principal
            filterSessions();
        }

        function filterSessions() {
            const sections = document.querySelectorAll('.day-section');
            const searchTerm = document.getElementById('speakerSearch').value.toLowerCase();
            
            // Regex para extrair o ID do palestrante do atributo onclick
            const speakerIdRegex = /showSpeaker\(['"]([^'"]+)['"]\)/;

            // Oculta todas as seções por padrão e limpa a animação
            sections.forEach(section => {
                section.classList.remove('active');
                section.querySelectorAll('.session-card').forEach(card => card.style.display = 'block');
                section.style.animation = 'none';
            });
            
            let hasVisibleSessions = false;

            sections.forEach(section => {
                const dayMatch = currentDayFilter === 'todos' || section.dataset.day === currentDayFilter;
                let daySessionsVisibleCount = 0;

                if (dayMatch) {
                    const sessionCards = section.querySelectorAll('.session-card');

                    sessionCards.forEach(card => {
                        const theme = card.querySelector('.session-theme').textContent.toLowerCase();
                        const onClickAttribute = card.getAttribute('onclick');
                        let allSpeakerInfo = '';

                        // Tenta extrair o ID do palestrante
                        const match = onClickAttribute.match(speakerIdRegex);

                        if (match && speakers[match[1]]) {
                            const speakerData = speakers[match[1]];
                            
                            // 1. Inclui o nome principal/nome do grupo (campo 'name')
                            allSpeakerInfo += speakerData.name.toLowerCase() + ' ';
                            
                            // 2. Inclui o tema
                            allSpeakerInfo += speakerData.theme.toLowerCase() + ' ';

                            // 3. INCLUI O CAMPO 'ABOUT' (ONDE ESTÃO OS NOMES EM GRUPO/RODA DE CONVERSA)
                            if (speakerData.about) {
                                allSpeakerInfo += speakerData.about.toLowerCase();
                            }
                        } else {
                            // Fallback para o nome visível no HTML
                            const speakerElement = card.querySelector('.speaker-name');
                            if (speakerElement) {
                                allSpeakerInfo += speakerElement.textContent.toLowerCase();
                            }
                        }
                        
                        // Verifica se o termo de busca corresponde a qualquer informação coletada
                        const searchMatch = searchTerm === '' || allSpeakerInfo.includes(searchTerm);

                        if (searchMatch) {
                            card.style.display = 'block';
                            daySessionsVisibleCount++;
                        } else {
                            card.style.display = 'none';
                        }
                    });

                    // Se houver sessões visíveis, mostre a seção do dia
                    if (daySessionsVisibleCount > 0) {
                        section.classList.add('active');
                        hasVisibleSessions = true;
                    }
                }
            });

            // Lógica para exibição final e animação
            if (currentDayFilter === 'todos' && searchTerm === '') {
                 sections.forEach(section => section.classList.add('active'));
            } else if (hasVisibleSessions) {
                sections.forEach(section => {
                    if(section.classList.contains('active')) {
                        section.style.animation = 'fadeIn 0.5s ease';
                    }
                });
            }
        }
        
        // Chamada inicial para garantir que todos os dias estejam visíveis ao carregar a página
        document.addEventListener('DOMContentLoaded', () => {
            filterSessions();
        });


        function showSpeaker(speakerId) {
            const speaker = speakers[speakerId];
            if (!speaker) return;

            document.getElementById('modalName').textContent = speaker.name;
            document.getElementById('modalRole').textContent = speaker.role;
            document.getElementById('modalTheme').textContent = speaker.theme;
            document.getElementById('modalAbout').textContent = speaker.about;
            
            const modalAvatar = document.getElementById('modalAvatar');

            // 1. Prioridade: Múltiplas Imagens (Grupos com fotos individuais)
            if (speaker.images && speaker.images.length > 0) {
                // Adiciona um container flex para as imagens e aplica o object-fit inline para corrigir a distorção
                modalAvatar.innerHTML = `<div style="display:flex; align-items:center; justify-content:center; gap:5px;">` + 
                                        speaker.images
                                            .map(imgSrc => `<img src="${imgSrc}" alt="${speaker.name}" 
                                                    style="width:60px; height:60px; border-radius:50%; 
                                                           object-fit:cover; object-position:center;">`)
                                            .join('') + 
                                        `</div>`;
            } 
            // 2. Segunda Prioridade: Imagem Única
            else if (speaker.image) {
                // A imagem única será ajustada pelo CSS .modal-avatar img que você adicionou
                modalAvatar.innerHTML = `<img src="${speaker.image}" alt="${speaker.name}">`; 
            } 
            // 3. Última Opção: Ícone (Grupos que não têm fotos, como a Roda de Conversa sem fotos no objeto)
            else {
                // Mostra o ícone de grupo (ou o ícone padrão)
                modalAvatar.innerHTML = `<i class="fas ${speaker.icon}"></i>`;
            }

            document.getElementById('speakerModal').classList.add('show');
            document.body.style.overflow = 'hidden';
        }

