        function filterDay(day) {
            const buttons = document.querySelectorAll('.filter-btn');
            buttons.forEach(btn => btn.classList.remove('active'));
            event.target.closest('.filter-btn').classList.add('active');

            const sections = document.querySelectorAll('.day-section');
            sections.forEach(section => {
                // Primeiro, remova a classe 'active' de todas as seções
                section.classList.remove('active');
            });

            if (day === 'todos') {
                // Se a opção for 'todos', adicione a classe 'active' a todas as seções
                sections.forEach(section => {
                section.classList.add('active');
                });
            } else {
                // Caso contrário, adicione a classe 'active' apenas à seção correspondente
                sections.forEach(section => {
                if (section.dataset.day === day) {
                    section.classList.add('active');
                }
                });
            }
        }

